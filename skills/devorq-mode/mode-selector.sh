#!/usr/bin/env bash
# devorq-mode/scripts/mode-selector.sh
# DetecГ§ГЈo automГЎtica ou manual do modo de implementaГ§ГЈo DEVORQ
#
# Uso:
#   devorq-mode/mode-selector.sh [auto|classic|<intent>]
#
# Se nenhum argumento: detecta automaticamente via keywords
# e exibe menu se ambiguo

set -euo pipefail

MODE="${1:-}"
PROJECT_ROOT="${2:-$(pwd)}"

# в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
# Helpers
# в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

info()   { echo "[DEVORQ-MODE] $*"; }
warn()   { echo "[DEVORQ-MODE] WARN: $*"; }
error()  { echo "[DEVORQ-MODE] ERROR: $*" >&2; exit 1; }

# в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
# DetecГ§ГЈo de modo via keywords
# в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

_is_auto() {
    [[ "${MODE,,}" =~ (auto|autonomous|ralph|story.by.story|modo.auto|autom.tico|story.by.story|story-by-story) ]]
}

_is_classic() {
    [[ "${MODE,,}" =~ (classic|manual|tradicional|direto|modo.classic|cl.ssico|implementar.direto) ]]
}

# в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
# ValidaГ§ГЈo do projeto
# в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

validate_project() {
    local spec="${PROJECT_ROOT}/SPEC.md"
    local devorq_dir="${PROJECT_ROOT}/.devorq"

    if [ ! -f "$spec" ]; then
        error "SPEC.md nГЈo encontrado em ${PROJECT_ROOT}"
    fi

    if [ ! -d "$devorq_dir" ]; then
        warn ".devorq/ nГЈo encontrado вЂ” executando devorq init..."
        devorq init 2>/dev/null || true
    fi

    return 0
}

# в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
# Mostrar status do projeto
# в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

show_project_status() {
    local ctx_file="${PROJECT_ROOT}/.devorq/state/context.json"

    echo ""
    echo "в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ"
    echo "вљЎ DEVORQ v3 вЂ” Mode Selector"
    echo "в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ"

    # Project name
    local project_name="${PROJECT_ROOT##*/}"
    if [ -f "$ctx_file" ]; then
        project_name=$(jq -r '.project // empty' "$ctx_file" 2>/dev/null || echo "${PROJECT_ROOT##*/}")
    fi
    echo " Project: $project_name"

    # Intent
    local intent=""
    if [ -f "$ctx_file" ]; then
        intent=$(jq -r '.intent // ""' "$ctx_file" 2>/dev/null || echo "")
    fi
    if [ -n "$intent" ]; then
        echo " Intent:  ${intent:0:60}..."
    else
        echo " Intent:  (nГЈo definido вЂ” use devorq context set intent)"
    fi

    # Stack
    local stack=""
    if [ -f "$ctx_file" ]; then
        stack=$(jq -r '.stack | join(", ") // ""' "$ctx_file" 2>/dev/null || echo "")
    fi
    if [ -n "$stack" ]; then
        echo " Stack:   [$stack]"
    fi

    # Stories (se AUTO)
    if [ -f "${PROJECT_ROOT}/prd.json" ]; then
        local total pending
        total=$(jq '.stories | length' "${PROJECT_ROOT}/prd.json" 2>/dev/null || echo "0")
        pending=$(jq '[.stories[] | select((.passes != true) and (.status != "done" and .status != "complete"))] | length' "${PROJECT_ROOT}/prd.json" 2>/dev/null || echo "0")
        echo " Stories: $pending pendentes / $total total"
    fi

    echo "в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ"
}

# в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
# Main logic
# в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

main() {
    # Se argumento fornecido, detectar modo
    if [ -n "$MODE" ]; then
        validate_project

        if _is_auto; then
            # Checar se intent contГ©m DDD keywords
            local ctx_file="${PROJECT_ROOT}/.devorq/state/context.json"
            local intent=""
            if [ -f "$ctx_file" ]; then
                intent=$(jq -r '.intent // ""' "$ctx_file" 2>/dev/null || echo "")
            fi
            if echo "$intent" | grep -qiE "domГ­nio|ddd|modelagem|entidade|contexto|bounded|invariante"; then
                warn "DDD detectado no intent + modo AUTO"
                info "SugestГЈo: considere usar CLASSIC ou rodar: devorq ddd explore"
            fi
            echo "MODE=AUTO"
            return 0
        elif _is_classic; then
            echo "MODE=CLASSIC"
            return 0
        fi

        # Modo nГЈo reconhecido в†’ perguntar
        warn "Modo '$MODE' nГЈo reconhecido"
    fi

    # Sem argumento в†’ mostrar menu (chamado via Hermes Agent)
    show_project_status

    echo ""
    echo "вљЎ Modo de implementaГ§ГЈo:"
    echo ""
    echo "  [1] рџ¤– AUTO вЂ” story por story"
    echo "      delegate_task, contexto limpo por iteracao,"
    echo "      commit por story, mais robusto"
    echo ""
    echo "  [2] рџ“ќ CLASSIC вЂ” gates 1-7 direto"
    echo "      implementacao direta, rapido,"
    echo "      ideal para tasks pequenas"
    echo ""
    echo "  [3] рџљЂ AUTO [N] stories"
    echo "      auto com limite especifico"
    echo ""

    # NГЈo exit aqui вЂ” Hermes Agent faz a pergunta real
    echo "MODE=ASK"
}

main "$@"
